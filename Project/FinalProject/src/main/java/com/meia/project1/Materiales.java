    public String Buscar(String Usuario, String NombreMaterial){
        File file = new File("C:\\MEIA\\Materiales.txt");
        String Retornar = "";
        if (file.exists()) {
            FileReader reader;
            try {
                reader = new FileReader(file);
                String linea;
                BufferedReader bReader = new BufferedReader(reader);
                while((linea=bReader.readLine())!=null){
                    String[] Material = new String[7];
                    Material = linea.split("\\|");
                    if(Material[0].equals(NombreMaterial) && Usuario.equals(Material[4])){
                        Retornar = linea;
                        break;
                    }
                }

                reader.close();
                bReader.close();
            }catch (IOException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return Retornar;
    }
    
    public String[] ReturnContent(String Archivo, int length){
        File file = new File("C:\\MEIA\\" + Archivo + ".txt");
        
        if (file.exists()) {
            FileReader reader;
            try {
                reader = new FileReader(file);
                BufferedReader bReader = new BufferedReader(reader);
                
                String linea;
                String[] retornar = new String[length];
                int i = 0;
                while((linea=bReader.readLine())!=null){
                    retornar[i] = linea;
                    i++;
                }
                reader.close();
                bReader.close();
                return retornar;
            }catch (IOException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        return null;
    }
    
