    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        if(MaterialBuscarField.getText().equals("") || NombreField.getText().equals("") || TipoField.getText().equals("") || TempoField.getText().equals("")){
            AlertLbl.setText("Debe Buscar el material antes de modificar");
        }
        else{
            AlertLbl.setText("");
            String linea_material = Buscar(ObtenerAdmin(), MaterialBuscarField.getText());
            String linea_original = linea_material;
            String[] Material = new String[7];
            Material = linea_material.split("\\|");
            
            Material[0] = NombreField.getText();
            Material[1] = TipoField.getText();
            Material[3] = TempoField.getText();
            
            linea_material = "";
            for (int i = 0; i < 7; i++) {
                if(i == 0)
                    linea_material += Material[0];
                else
                    linea_material += "|" + Material[i];
            }
            
            //Arreglo Dinamico leer bitacora
            int contador =0;
            File file = new File("C:\\MEIA\\Materiales.txt");
            if (file.exists()) {
                FileReader reader;
                try {
                    reader = new FileReader(file);
                    String linea;
                    BufferedReader bReader = new BufferedReader(reader);
                    while((linea=bReader.readLine())!=null){
                        contador++;
                    }

                    reader.close();
                    bReader.close();

                }catch (IOException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            String[] ListUsers = ReturnContent("Materiales", contador);

            //Escribir en bitacora
            FileWriter fichero = null;
            PrintWriter pw = null;
            try
            {
                fichero = new FileWriter("C:\\MEIA\\Materiales.txt");
                pw = new PrintWriter(fichero);
                for(int i = 0; i < contador; i++){
                    if(ListUsers[i].equals(linea_original))
                        pw.println(linea_material);
                    else
                        pw.println(ListUsers[i]);
                }

                fichero.close();
                pw.close();                                                
            } catch (Exception e) {
                e.printStackTrace();
            }
            Retornar_vacio();
            AlertLbl.setText("Editado correctamente");
        }
    }//GEN-LAST:event_EditBtnActionPerformed

