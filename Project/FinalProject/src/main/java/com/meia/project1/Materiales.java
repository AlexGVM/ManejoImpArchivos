    private void IngresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarBtnActionPerformed
        // Ingresar nuevo material
        String Nombre_Material = NombreField.getText();
        String Tipo = TipoField.getText();
        String path = PathField.getText();
        String Tiempo_degradarse = TempoField.getText();
        String Usuario = ObtenerAdmin();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
        String FechaCreacion = sdf.format(new Date());
        String Status = "1";
        ManejoArchivoArbol();
        
        if(!Nombre_Material.equals("") && !Tipo.equals("") && !path.equals("") && !Tiempo_degradarse.equals("")){
            //Crear usuario
            //Crea trama de Nombre_Material|Tipo|Path_imagen
            String Material = Nombre_Material + "|" + Tipo + "|" + path;
            //Crea trama de |Tipo_para_degradarse|Usuario_Transaccion|fecha_Creacion
            Material += "|" + Tiempo_degradarse + "|" + Usuario + "|" + FechaCreacion;
            //Crea trama de |Status
            Material += "|" + Status;
            
            //Arreglo Dinamico leer bitacora
            int contador =0;
            File file = new File("C:\\MEIA\\Materiales.txt");
            if (file.exists()) {
                FileReader reader;
                try {
                    reader = new FileReader(file);
                    String linea;
                    BufferedReader bReader = new BufferedReader(reader);
                    while((linea=bReader.readLine())!=null){
                        contador++;
                    }

                    reader.close();
                    bReader.close();

                }catch (IOException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            String[] ListUsers = ReturnContent("Materiales", contador);

            //Escribir en bitacora
            FileWriter fichero = null;
            PrintWriter pw = null;
            try
            {
                fichero = new FileWriter("C:\\MEIA\\Materiales.txt");
                pw = new PrintWriter(fichero);
                for(int i = 0; i < contador; i++){
                    pw.println(ListUsers[i]);
                }
                pw.println(Material);

                fichero.close();
                pw.close(); 
                Retornar_vacio();
                AlertLbl.setText("Ingresado correctamente");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_IngresarBtnActionPerformed

